pr = float(input("enter the percentage : "))


if pr >= 90:
    print("Grade A")
elif pr >= 80:
    print("Grade B")
elif pr >= 70:
    print("Grade C")
elif pr >= 60:
    print("Grade D")
elif pr >= 50:
    print("Grade E")
else:
    print("Grade F")



