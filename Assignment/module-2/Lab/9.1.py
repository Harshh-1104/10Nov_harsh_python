'''Write a Python program to import the math module and use functions like sqrt(), ceil(), 
floor()'''

import math

num1 = 25
num2 = 11.4


print(f"Square root of {num1} = {math.sqrt(num1)}")

print(f"Ceil of {num2} = {math.ceil(num2)}")        #top round figure value
print(f"Floor of {num2} = {math.floor(num2)}")         #top bottom figure value

